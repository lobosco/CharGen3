
public class Weapon {
	
	 String Name;
     int Break;
     int Attack;
     int Distance;
     int Block;
     int Value;
     String Material;
     
     public Weapon(String Name, int Break, int Attack, int Distance, int Block, int Value, String Material){
    	 this.Name = Name;
    	 this.Break = Break;
    	 this.Attack = Attack;
    	 this.Distance = Distance;
    	 this.Block = Block;
    	 this.Value = Value;
    	 this.Material = Material;
     }
	

}
