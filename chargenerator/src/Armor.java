
public class Armor {
	
	String Name;
    String Sort;
    String Material;
    int Protection;
    int Break;
    
    public Armor(String Name, String Sort, String Material, int Protection, int Break){
    	this.Name = Name;
    	this.Sort = Sort;
    	this.Material = Material;
    	this.Protection = Protection;
    	this.Break = Break;
    }
    

}
